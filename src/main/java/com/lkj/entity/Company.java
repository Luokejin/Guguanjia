package com.lkj.entity;

public class Company {
	private int companyId;
	private String companyProcessAbility;
	private String companyPower;
	private String companyIntroduction;
	private int companyCallnum;
	private String companyName;
	
	public int getCompanyId() {
		return companyId;
	}
	public void setCompanyId(int companyId) {
		this.companyId = companyId;
	}
	public String getCompanyProcessAbility() {
		return companyProcessAbility;
	}
	public void setCompanyProcessAbility(String companyProcessAbility) {
		this.companyProcessAbility = companyProcessAbility;
	}
	public String getCompanyPower() {
		return companyPower;
	}
	public void setCompanyPower(String companyPower) {
		this.companyPower = companyPower;
	}
	public String getCompanyIntroduction() {
		return companyIntroduction;
	}
	public void setCompanyIntroduction(String companyIntroduction) {
		this.companyIntroduction = companyIntroduction;
	}
	public int getCompanyCallnum() {
		return companyCallnum;
	}
	public void setCompanyCallnum(int companyCallnum) {
		this.companyCallnum = companyCallnum;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	
	
	
}
